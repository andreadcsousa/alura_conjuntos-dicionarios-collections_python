# Filtrando valores
"Sérgio precisa implementar um dicionário que tenha o nome e a idade de seus clientes, para saber a faixa etária deles. Esta implementação terá mil registros. Após fazer isto, ele deve verificar alguns clientes. Como ele pode fazer isto?"

dict.get(key, value)

# O código acima é a sintaxe da função get, onde "dict" poderia ser o conjunto de clientes de Sérgio e a "key" poderia ser o range de idade deles.
